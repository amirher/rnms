copy_rrds() {
	for from_path in `ls ${JFFNMS_PATH}/interface-$1-*.rrd` ; do
		index=`expr "$from_path" : '.*/interface-[0-9]*-\([0-9]*\).rrd'`
		to_path=`echo "${TO}/${2}-${index}.rrd"`
		cp $from_path $to_path
	done
}

for id in $IDS ; do
	from_id=`expr "$id" : '\([0-9]*\):'`
	to_id=`expr "$id" : '.*:\([0-9]*\)'`
	copy_rrds $from_id $to_id
done
